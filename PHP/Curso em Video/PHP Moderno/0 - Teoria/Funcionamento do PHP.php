                                         Funcionamento do PHP


* Utiliza em conjunto JavaScript, HTML e CSS -> FrontEnd (Cliente) 
* PHP -> Backend (Servidor)

                        Tecnologias de Funcionamento
Cliente Side
    |- Response = Envia uma copia dos arquivos para serem renderizados no Cliente

Server Side
    |- Response = Vai interpretar o script php e entregar o resultado em html e JavaScript, que serão entregues para o cliente

                        Fluxograma

Client Side -> Cliente faz requisição(request) para o Servidor DNS
 |-> Servidor DNS acessa o Servidor solicitado

Server Side ->  Servidor Responde o Servidor DNS
 |-> Servidor DNS retorna a informação solicitada para o cliente


                
