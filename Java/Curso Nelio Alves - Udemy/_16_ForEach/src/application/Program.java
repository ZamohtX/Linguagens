package application;

public class Program {
	/*
	 * 				ForEach
	 * 
	 * 	º Sintaxe opcional para iterar coleções
	 * 
	 * 
	 * 
	 * 
	 * 
	 * 
	 * 
	 * 
	 * */
	public static void main(String[] args) {
		
		String[] vect = new String[] {"Thomaz","Lilian", "Davi"};
		
		// Laço forEach
		for (String valor : vect) {
			System.out.println(valor);
		}
		
	}

}
