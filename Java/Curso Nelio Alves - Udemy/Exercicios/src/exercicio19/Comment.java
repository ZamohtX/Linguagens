package exercicio19;

public class Comment {
	// Atributos
	private String text;
	
	// Constutores
	public Comment() {
		
	}
	public Comment(String text) {
		this.text = text;
	}
	// Getters e Setters
	public String getText() {
		return this.text;
	}
	public void setText(String text) {
		this.text = text;
	}

}
