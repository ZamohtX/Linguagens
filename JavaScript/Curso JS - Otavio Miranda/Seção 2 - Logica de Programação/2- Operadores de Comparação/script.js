//      Operadores de Comparação

/*
            >     - Maior 
            >=    - Maior ou Igual 
            <     - Menor 
            <=    - Menor ou Igual 
            ==    - Igual               -( checa valor )
            ===   - Igualdade Estrita   -( checa valor e tipo )
            !=    = Diferente           -( checa valor )    
            !==   - Diferente Estrito   -( checa valor e tipo )
*/

//  Expressões:
console.log(10 > 5);
console.log(10 >= 5);
console.log(10 < 5);
console.log(10 <= 5);
console.log(10 == 5);
console.log(10 == '10'); //     NAO CHECA O TIPO
console.log(10 === '10');//     Checa o Tipo
console.log(10 != '10')//       NAO CHECA O TIPO
console.log(10 !== '10');//     Checa o tipo
