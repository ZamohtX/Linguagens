


// While ->  Estrutura de repetição com Teste Logico no Inicio
var c = 1;
while (c <= 6) {
    console.log(`Passo ${c}`);
    c++
}

// Do while -> Estrutura de repetição com Teste Logico no Final
var c = 1;
do {
    console.log(`Passo ${c}`)
    c++;
} while (c <= 6);


// For -> Estrutura de repetição com variavel de controle
for (var c = 1; c <= 5; c++) {
    console.log(c)
}